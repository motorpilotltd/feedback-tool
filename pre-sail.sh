#!/usr/bin/env sh

docker info > /dev/null 2>&1

# Ensure that Docker is running...
if [ $? -ne 0 ]; then
    echo "Docker is not running."

    exit 1
fi

# Install dependencies
docker run --rm \
    -u "$(id -u):$(id -g)" \
    -v "$(pwd):/var/www/html" \
    -w /var/www/html \
    laravelsail/php83-composer:latest \
    bash -c "export COMPOSER_PROCESS_TIMEOUT=1200 && composer install --no-cache --ignore-platform-reqs"
